#!/usr/bin/env python3
"""
Exercise 0: Planting Your First Seed.
"""


def main() -> None:
    """
    The main function of the program.
    """
    name: str = "Rose"
    height: float = 25.0
    age: int = 30
    print("=== Welcome to My Garden ===")
    print(f"Plant: {name}")
    print(f"Height: {height}cm")
    print(f"Age: {age} days")
    print("\n=== End of Program ===")


if __name__ == "__main__":
    main()
